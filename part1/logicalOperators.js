// Logical operators ( && ,|| )

console.log(10 > 3 && 3 > 4); //false
console.log(3 < 5 && 6 > 5); //true

//for logical AND operators both the conditions should be true to be a final result true  (true && true =true) otherwise false 


console.log(34 < 3 || 3 > 4); //false
console.log(2 < 4 || 2 > 4); //true

//for logical OR operators if any one of the condition become true the final result become ture if both conditions are false the final result will be false 



